num01 = int(input('Digite o primeiro número: '))
num02 = int(input('Digite o segundo número: '))
operador = input('Digite o operador (+,-,*, /): ')

if operador == '+' :
    print(num01 + num02)
elif operador == '-':
    print(num01 - num02)
elif operador == '*':
    print(num01 * num02)
elif operador == '/':
    print(num01 / num02)

